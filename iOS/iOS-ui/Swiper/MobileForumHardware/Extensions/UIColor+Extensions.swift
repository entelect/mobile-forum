import UIKit

extension UIColor {
    static func darkRed() -> UIColor {
        return UIColor(red: 120/255, green: 2/255, blue: 6/255, alpha: 1)
    }
    
    static func darkBlue() -> UIColor {
        return UIColor(red: 6/255, green: 17/255, blue: 97/255, alpha: 1)
    }
    
    static func maroon() -> UIColor {
        return UIColor(red: 82/255, green: 1/255, blue: 4/255, alpha: 1)
    }
}
